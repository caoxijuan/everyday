// 需求: 利用JavaScript完成输出如下图形（由“空格”和“*”这两种符号组成）
//     *********
//    *******
//   *****
//  ***      
// *  


//分析：空格： 4 3 2 1    * 9 7 5 3 1

for (var a=0;a<5;a++){
    var str=""
    //b: 4 3 2 1
    for (b=0;b<5-a;b++){
        str+=" "
    }
    //c:9 7 5 3 1
    for (var c=0;c<=-(2 * a + 1)+9;c++){
        str+="*"
    }
    console.log(str)
}